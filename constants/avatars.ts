export const OUTFIT_COLORS = ['#7F77DD','#1D9E75','#D85A30','#378ADD','#D4537E','#639922','#888780','#BA7517'];
export const HAT_COLORS = ['#7F77DD','#D85A30','#1D9E75','#378ADD','#888780','#2c1b0e'];

export interface AvatarConfig {
  avatarId: AvatarId;
  skinIdx: number;
  hairIdx: number;
  outfitIdx: number;
  hatIdx: number;
}

export const DEFAULT_CONFIG: AvatarConfig = {
  avatarId: 'wanderer',
  skinIdx: 0,
  hairIdx: 0,
  outfitIdx: 0,
  hatIdx: 0,
};

export function getColorMap(config: AvatarConfig): Record<number, string> {
  const outfit = OUTFIT_COLORS[config.outfitIdx];
  const hat = HAT_COLORS[config.hatIdx];
  // Darken outfit slightly for legs/shadow
  return {
    0: 'transparent',
    1: '#1a1a2e',
    2: SKIN_TONES[config.skinIdx],
    3: HAIR_COLORS[config.hairIdx],
    4: outfit,
    5: '#2c2c4a',
    6: hat,
    7: '#1a1a2e',
    8: hat,
  };
}
